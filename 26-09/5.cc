
#include <iostream>

int main() {
    int n; 
    std::cout << "Insert a number, any number: ";
    std::cin >> n;

    std::cout << n << " times 0 is: " << n * 0 << std::endl;
    std::cout << n << " times 1 is: " << n * 1 << std::endl;
    std::cout << n << " times 2 is: " << n * 2 << std::endl;
    std::cout << n << " times 3 is: " << n * 3 << std::endl;
    std::cout << n << " times 4 is: " << n * 4 << std::endl;
    std::cout << n << " times 5 is: " << n * 5 << std::endl;
    std::cout << n << " times 6 is: " << n * 6 << std::endl;
    std::cout << n << " times 7 is: " << n * 7 << std::endl;
    std::cout << n << " times 8 is: " << n * 8 << std::endl;
    std::cout << n << " times 9 is: " << n * 9 << std::endl;
    std::cout << n << " times 10 is: " << n * 10 << std::endl;

    return 0;
}