#include <iostream>

using namespace std;

int main() {
    int n; 
    cout << "Insert a number, any number: ";
    cin >> n;

    cout << n << " times 0 is: " << n * 0 << endl;
    cout << n << " times 1 is: " << n * 1 << endl;
    cout << n << " times 2 is: " << n * 2 << endl;
    cout << n << " times 3 is: " << n * 3 << endl;
    cout << n << " times 4 is: " << n * 4 << endl;
    cout << n << " times 5 is: " << n * 5 << endl;
    cout << n << " times 6 is: " << n * 6 << endl;
    cout << n << " times 7 is: " << n * 7 << endl;
    cout << n << " times 8 is: " << n * 8 << endl;
    cout << n << " times 9 is: " << n * 9 << endl;
    cout << n << " times 10 is: " << n * 10 << endl;

    return 0;
}